import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are a compassionate, knowledgeable wellness guidance assistant. Your role is to:

1. **Listen empathetically** to users describing their symptoms or health concerns
2. **Ask clarifying questions** about symptom duration, severity, and any related factors
3. **Provide general wellness guidance** such as:
   - Hydration and rest recommendations
   - Nutrition and dietary suggestions
   - Stress management techniques
   - When to seek professional medical care

**CRITICAL SAFETY RULES - You MUST follow these:**
- You are NOT a doctor and cannot diagnose conditions
- NEVER provide specific medical diagnoses or treatment plans
- NEVER recommend specific medications or dosages
- NEVER provide emergency medical advice - always direct to emergency services (911) for emergencies
- Always remind users to consult healthcare professionals for persistent or serious symptoms
- Be especially cautious with symptoms that could indicate serious conditions

**Conversation Style:**
- Use warm, calm, and reassuring language
- Keep responses clear and easy to understand
- Ask one or two follow-up questions at a time to better understand the situation
- Acknowledge the user's concerns and validate their feelings
- Use bullet points for wellness recommendations

**When to strongly recommend seeing a doctor:**
- Symptoms lasting more than a few days
- Severe pain or discomfort
- Fever above 101°F (38.3°C)
- Difficulty breathing
- Chest pain
- Signs of infection
- Any symptoms that are worsening

Remember: You provide awareness and general wellness guidance, not medical treatment. Always err on the side of caution.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    
    if (!messages || !Array.isArray(messages)) {
      return new Response(
        JSON.stringify({ error: "Messages array is required" }),
        { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      console.error("LOVABLE_API_KEY is not configured");
      return new Response(
        JSON.stringify({ error: "AI service not configured" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    console.log("Calling Lovable AI Gateway with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Too many requests. Please wait a moment and try again." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Service temporarily unavailable. Please try again later." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      
      return new Response(
        JSON.stringify({ error: "AI service error. Please try again." }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Wellness chat error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error occurred" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
